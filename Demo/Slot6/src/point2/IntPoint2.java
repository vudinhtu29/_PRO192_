package point2;

/**
 *
 * @author ADMIN
 */
public class IntPoint2 {
    public int x;
    public int y;

    public IntPoint2() {
    }

    public IntPoint2(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
}
